-- Миграция для изменения типа поля quantity с INTEGER на DECIMAL
-- Это позволит сохранять дробные количества (например, 0.5 порции или 1.5 кг)
-- Существующие данные НЕ потеряются, целые числа просто станут дробными (1 -> 1.0)

ALTER TABLE public.bill_items 
ALTER COLUMN quantity TYPE DECIMAL(10, 3);

